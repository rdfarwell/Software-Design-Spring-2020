public class Video {

    private String url, title, name;

    public Video(String url, String title, String name) {
        this.url = url;
        this.title = title;
        this.name = name;
    }

    public String getURL() {
        return url;
    }

    public String getTitle() {
        return title;
    }

    public String getName() {
        return name;
    }

}
